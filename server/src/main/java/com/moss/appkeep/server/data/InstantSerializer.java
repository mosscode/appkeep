package com.moss.appkeep.server.data;

import org.joda.time.Instant;

import com.moss.bdbwrap.Serializer;

public class InstantSerializer implements Serializer<Instant> {
	public Instant deSerialize(byte[] data) {
		if(data==null || data.length==0){
			return null;
		}else{
			return new Instant(readLong(data));
		}
	}
	
    public final byte[] writeLong(long v) {
    	byte[] writeBuffer = new byte[8];
        writeBuffer[0] = (byte)(v >>> 56);
        writeBuffer[1] = (byte)(v >>> 48);
        writeBuffer[2] = (byte)(v >>> 40);
        writeBuffer[3] = (byte)(v >>> 32);
        writeBuffer[4] = (byte)(v >>> 24);
        writeBuffer[5] = (byte)(v >>> 16);
        writeBuffer[6] = (byte)(v >>>  8);
        writeBuffer[7] = (byte)(v >>>  0);
        return writeBuffer;
    }
    
    public final long readLong(byte[] readBuffer) {
        return (((long)readBuffer[0] << 56) +
                ((long)(readBuffer[1] & 255) << 48) +
                ((long)(readBuffer[2] & 255) << 40) +
                ((long)(readBuffer[3] & 255) << 32) +
                ((long)(readBuffer[4] & 255) << 24) +
                ((readBuffer[5] & 255) << 16) +
                ((readBuffer[6] & 255) <<  8) +
                ((readBuffer[7] & 255) <<  0));
    }
	public byte[] serialize(Instant i) {
		return i==null?null:writeLong(i.getMillis());
	}
}
